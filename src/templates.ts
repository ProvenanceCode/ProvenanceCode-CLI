/**
 * Returns the Node.js script used as the preToolUse hook for Cursor
 * and the PreToolUse hook for Claude Code.
 *
 * Behaviour:
 *  1. Finds the nearest provenance.config.json (walks up from the file being
 *     edited, or falls back to repo root). Handles monorepo layouts by
 *     searching the whole repo when needed.
 *  2. Reads the last 5 decisions (sorted by mtime) and counts open risks.
 *  3. Returns an `agent_message` so the agent sees current provenance context
 *     before making changes — without blocking any tool call.
 */
export function getProvenanceContextCheckScript(): string {
  return `#!/usr/bin/env node
'use strict';
// prvc-context-check.js — injected by ProvenanceCode DEO v1.0 starter pack
// Fires on preToolUse (Cursor) / PreToolUse (Claude Code) for Write/Shell tools.
// Reads provenance context and returns it as agent_message. Never blocks.

const fs = require('fs');
const path = require('path');

function readStdin() {
  return new Promise(resolve => {
    let data = '';
    process.stdin.setEncoding('utf8');
    process.stdin.on('data', chunk => { data += chunk; });
    process.stdin.on('end', () => resolve(data));
  });
}

function getRepoRoot() {
  try {
    const { execSync } = require('child_process');
    return execSync('git rev-parse --show-toplevel', { encoding: 'utf8', stdio: ['pipe','pipe','pipe'] }).trim();
  } catch {
    return process.cwd();
  }
}

// Walk up from startDir looking for provenance/provenance.config.json
function findNearestConfig(startDir, repoRoot) {
  let dir = path.resolve(startDir);
  const stop = path.resolve(repoRoot);
  while (true) {
    const candidate = path.join(dir, 'provenance', 'provenance.config.json');
    if (fs.existsSync(candidate)) return candidate;
    if (dir === stop) break;
    const parent = path.dirname(dir);
    if (parent === dir) break;
    dir = parent;
  }
  return null;
}

// Monorepo: find ALL provenance.config.json files under repoRoot (depth <= 4)
function findAllConfigs(repoRoot) {
  const results = [];
  function walk(dir, depth) {
    if (depth > 4) return;
    let entries;
    try { entries = fs.readdirSync(dir); } catch { return; }
    for (const entry of entries) {
      if (entry === 'node_modules' || entry === '.git' || entry === 'dist') continue;
      const full = path.join(dir, entry);
      let stat;
      try { stat = fs.statSync(full); } catch { continue; }
      if (entry === 'provenance' && stat.isDirectory()) {
        const cfg = path.join(full, 'provenance.config.json');
        if (fs.existsSync(cfg)) { results.push(cfg); continue; }
      }
      if (stat.isDirectory()) walk(full, depth + 1);
    }
  }
  walk(repoRoot, 0);
  return results;
}

function safeReadJson(filePath) {
  try { return JSON.parse(fs.readFileSync(filePath, 'utf8')); } catch { return null; }
}

function buildContext(configPath, repoRoot) {
  const config = safeReadJson(configPath);
  if (!config) return null;

  const decisionsRel = (config.paths && config.paths.decisions) || 'provenance/decisions';
  const risksRel     = (config.paths && config.paths.risks)     || 'provenance/risks';

  // Paths are relative to the package root (parent of provenance/)
  const packageRoot  = path.dirname(path.dirname(configPath)); // provenance/../
  const decisionsDir = path.resolve(packageRoot, decisionsRel);
  const risksDir     = path.resolve(packageRoot, risksRel);

  const lines = [];

  // Recent decisions — sort by mtime, take 5
  if (fs.existsSync(decisionsDir)) {
    const files = fs.readdirSync(decisionsDir)
      .filter(f => f.endsWith('.json') && f !== 'TEMPLATE.json')
      .map(f => {
        let mtime = 0;
        try { mtime = fs.statSync(path.join(decisionsDir, f)).mtimeMs; } catch {}
        return { f, mtime };
      })
      .sort((a, b) => b.mtime - a.mtime)
      .slice(0, 5)
      .map(x => x.f);

    for (const f of files) {
      const d = safeReadJson(path.join(decisionsDir, f));
      if (!d) continue;
      const id    = d.id || d.decision_id || '?';
      const state = (d.lifecycle && d.lifecycle.state) || d.status || '?';
      const title = d.title || '?';
      lines.push('  • ' + id + ': ' + title + ' [' + state + ']');
    }
  }

  // Open risks
  let openRisks = 0;
  if (fs.existsSync(risksDir)) {
    for (const f of fs.readdirSync(risksDir).filter(x => x.endsWith('.json') && x !== 'TEMPLATE.json')) {
      const r = safeReadJson(path.join(risksDir, f));
      if (r && r.status === 'open') openRisks++;
    }
  }

  if (lines.length === 0 && openRisks === 0) return null;

  const parts = [];
  const label = packageRoot === repoRoot ? '' : ' (' + path.relative(repoRoot, packageRoot) + ')';
  if (lines.length > 0) {
    parts.push('📋 ProvenanceCode' + label + ' — recent decisions:\\n' + lines.join('\\n'));
  }
  if (openRisks > 0) {
    parts.push('⚠️  ' + openRisks + ' open risk(s) in ' + path.relative(repoRoot, risksDir) + '/');
  }
  return parts.join('\\n\\n');
}

async function main() {
  const raw = await readStdin();
  let input = {};
  try { input = JSON.parse(raw); } catch {}

  const repoRoot = getRepoRoot();

  // Extract the file path from the tool input (Cursor and Claude Code both provide it)
  const toolInput = input.tool_input || input.input || {};
  const filePath  = toolInput.path || toolInput.file_path || toolInput.target_file || '';

  let configPath = null;

  if (filePath) {
    const abs = path.isAbsolute(filePath) ? filePath : path.resolve(process.cwd(), filePath);
    configPath = findNearestConfig(path.dirname(abs), repoRoot);
  }

  // Fallback: repo root config
  if (!configPath) {
    const rootCfg = path.join(repoRoot, 'provenance', 'provenance.config.json');
    if (fs.existsSync(rootCfg)) configPath = rootCfg;
  }

  // Monorepo fallback: find all, pick the one with the most recent activity
  let allConfigs = [];
  if (!configPath) {
    allConfigs = findAllConfigs(repoRoot);
    if (allConfigs.length > 0) configPath = allConfigs[0];
  } else {
    allConfigs = [configPath];
  }

  if (!configPath) {
    process.stdout.write(JSON.stringify({ permission: 'allow' }));
    return;
  }

  // In monorepo mode, build context for every package that has a config
  const ctxParts = [];
  for (const cfg of allConfigs) {
    const ctx = buildContext(cfg, repoRoot);
    if (ctx) ctxParts.push(ctx);
  }

  if (ctxParts.length === 0) {
    process.stdout.write(JSON.stringify({ permission: 'allow' }));
    return;
  }

  const footer = '\\n\\nTo record a decision: prvc journal add "Title" --outcome="..." --rationale="..." --risk-level=low --bot=cursor-ai';
  const agentMessage = ctxParts.join('\\n\\n---\\n\\n') + footer;

  process.stdout.write(JSON.stringify({
    permission: 'allow',
    agent_message: agentMessage
  }));
}

main().catch(() => process.stdout.write(JSON.stringify({ permission: 'allow' })));
`;
}

