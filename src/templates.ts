/**
 * Create Cursor AI starter pack.
 *
 * Installs:
 *  - provenance/ai/cursor/ — rules, prompts (documentation)
 *  - .cursor/hooks.json    — preToolUse context injection + stop session-end
 *  - .cursor/hooks/prvc-context-check.js  — the hook script
 *
 * Merges into any existing .cursor/hooks.json instead of clobbering it.
 */
export function createCursorStarterPack(baseDir: string, appCode: string, area: string): void {
  // 1. Documentation pack (provenance/ai/cursor/)
  const cursorDir = path.join(baseDir, 'provenance', 'ai', 'cursor');
  const promptsDir = path.join(cursorDir, 'prompts');
  fs.ensureDirSync(promptsDir);
  fs.writeFileSync(path.join(cursorDir, 'rules.md'), getCursorRules(appCode, area));
  fs.writeFileSync(path.join(promptsDir, 'create-decision.md'), getCursorCreateDecisionPrompt());
  fs.writeFileSync(path.join(promptsDir, 'create-risk.md'), getCursorCreateRiskPrompt());
  fs.writeFileSync(path.join(promptsDir, 'pr-review.md'), getCursorPRReviewPrompt());

  // 2. Cursor hooks (.cursor/)
  const cursorHooksDir = path.join(baseDir, '.cursor', 'hooks');
  fs.ensureDirSync(cursorHooksDir);

  // Write the context-check script
  const scriptPath = path.join(cursorHooksDir, 'prvc-context-check.js');
  fs.writeFileSync(scriptPath, getProvenanceContextCheckScript());
  try { fs.chmodSync(scriptPath, '755'); } catch {}

  // Merge into hooks.json
  const hooksJsonPath = path.join(baseDir, '.cursor', 'hooks.json');
  let existing: any = { version: 1, hooks: {} };
  if (fs.existsSync(hooksJsonPath)) {
    try { existing = fs.readJsonSync(hooksJsonPath); } catch {}
  }
  const merged = buildCursorHooksJson(existing);
  fs.writeJsonSync(hooksJsonPath, merged, { spaces: 2 });
}

