/**
 * Returns the Cursor .cursor/hooks.json content.
 * Designed to be merged into any existing hooks.json rather than overwrite.
 */
export function buildCursorHooksJson(existing: any): any {
  const updated = JSON.parse(JSON.stringify(existing || { version: 1, hooks: {} }));
  if (!updated.version) updated.version = 1;
  if (!updated.hooks) updated.hooks = {};

  // --- preToolUse: context injection on Write / StrReplace ---
  if (!Array.isArray(updated.hooks.preToolUse)) updated.hooks.preToolUse = [];
  // Remove any existing prvc entry before re-adding
  updated.hooks.preToolUse = updated.hooks.preToolUse.filter(
    (h: any) => !String(h.command || '').includes('prvc-context-check')
  );
  updated.hooks.preToolUse.push({
    command: '.cursor/hooks/prvc-context-check.js',
    matcher: 'Write|StrReplace',
    timeout: 8
  });

  // --- stop: session-end provenance check (prompt hook) ---
  if (!Array.isArray(updated.hooks.stop)) updated.hooks.stop = [];
  updated.hooks.stop = updated.hooks.stop.filter(
    (h: any) => !String(h.prompt || '').includes('ProvenanceCode')
  );
  updated.hooks.stop.push({
    type: 'prompt',
    prompt: getCursorSessionEndPrompt(),
    timeout: 60,
    loop_limit: 1
  });

  return updated;
}

/**
 * Returns the Claude Code PreToolUse hook script (bash wrapper around the
 * shared Node.js context check script). Placed at .claude/hooks/PreToolUse.js
 */
export function getClaudePreToolUseHookScript(): string {
  // Claude Code passes the same JSON-on-stdin contract as Cursor.
  // We reuse the identical Node.js script — just change the bot name in the footer.
  return getProvenanceContextCheckScript().replace(
    '--bot=cursor-ai',
    '--bot=claude-code'
  );
}

